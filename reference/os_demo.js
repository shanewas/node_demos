const os = require('os');

//platform
console.log(os.platform());

//CPU arch
console.log(os.arch());

//cpu code info
console.log(os.cpus());

//Free memory
console.log(os.freemem());

//total memory
console.log(os.totalmem());

//Home dir
console.log(os.homedir());

//uptime
console.log(os.uptime());